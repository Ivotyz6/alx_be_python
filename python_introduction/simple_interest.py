#define principal
principal = 1000  # Principal amount in dollars
# define rate of interest
rate = 0.05  # Rate of interest in percent per annum
# define time period    
time = 3  # Time period in years
# calculate simple interest
simple_interest = (principal * rate * time)    
# print the result
print(f"The simple interest for a principal of ${principal}, rate of interest of {rate}, over a period of {time} years is: ${simple_interest}")